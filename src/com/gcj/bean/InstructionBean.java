package com.gcj.bean;

public class InstructionBean {

    private int productId;

    private String keyWords;

    private String name;

    private String manager;

    private String engineer;

    private int clientType;

    private String cmd;

    private String noVariablePara;

    private String variablePara;

    private String outputPara;

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

    public String getKeyWords() {
        return keyWords;
    }

    public void setKeyWords(String keyWords) {
        this.keyWords = keyWords;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getManager() {
        return manager;
    }

    public void setManager(String manager) {
        this.manager = manager;
    }

    public String getEngineer() {
        return engineer;
    }

    public void setEngineer(String engineer) {
        this.engineer = engineer;
    }

    public int getClientType() {
        return clientType;
    }

    public void setClientType(int clientType) {
        this.clientType = clientType;
    }

    public String getCmd() {
        return cmd;
    }

    public void setCmd(String cmd) {
        this.cmd = cmd;
    }

    public String getNoVariablePara() {
        return noVariablePara;
    }

    public void setNoVariablePara(String noVariablePara) {
        this.noVariablePara = noVariablePara;
    }

    public String getVariablePara() {
        return variablePara;
    }

    public void setVariablePara(String variablePara) {
        this.variablePara = variablePara;
    }

    public String getOutputPara() {
        return outputPara;
    }

    public void setOutputPara(String outputPara) {
        this.outputPara = outputPara;
    }
}
